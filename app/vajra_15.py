class VajraStage2Builder:

    def __init__(self, dataset):
        self.dataset = dataset

        # 🔥 VALID STATES
        self.valid_states = set([
            "andhra", "arunachal", "assam", "bihar", "chhattisgarh",
            "goa", "gujarat", "haryana", "himachal", "jharkhand",
            "karnataka", "kerala", "madhya", "maharashtra", "manipur",
            "meghalaya", "mizoram", "nagaland", "odisha", "punjab",
            "rajasthan", "sikkim", "tamil", "telangana", "tripura",
            "uttar", "uttarakhand", "west", "delhi", "jammu", "ladakh"
        ])

    # ===============================
    # 🔹 CLEAN TEXT
    # ===============================
    def clean_text(self, text):
        text = str(text).lower()
        text = " ".join(text.split())

        text = text.replace("percent", "%")
        text = text.replace("billion cubic meters", "bcm")
        text = text.replace("over exploited", "over-exploited")

        text = re.sub(r'(\d+)\s*\.\s*(\d+)', r'\1.\2', text)

        return text

    # ===============================
    # 🔥 SENTENCE SPLIT
    # ===============================
    def split_sentences(self, text):
        sentences = re.split(r'[.?!]', text)
        return [s.strip() for s in sentences if len(s.strip().split()) >= 5]

    # ===============================
    # 🔥 STRUCTURE FROM DOMAIN (KEY FIX)
    # ===============================
    def extract_structured_from_domain(self, text):

        structured = []
        text = text.lower()

        matches = re.findall(
            r'(\w+)\s+in\s+(\w+)\s+contains.*?(safe|critical|semi-critical|over-exploited)',
            text
        )

        for district, state, category in matches:

            if state not in self.valid_states:
                continue

            if len(district) < 3:
                continue

            structured.append({
                "query": f"groundwater status of {district} in {state}",
                "response": f"{district} in {state} contains units classified as {category}",
                "expert_tag": "structured"
            })

        return structured

    # ===============================
    # 🔹 QUERY REWRITE
    # ===============================
    def rewrite_query(self, query, response):

        words = response.split()

        for i in range(len(words) - 2):
            if words[i+1] == "in":
                return f"groundwater status of {words[i]} in {words[i+2]}"

        return "groundwater classification based on extraction"

    # ===============================
    # 🔹 LENGTH FILTER
    # ===============================
    def valid_length(self, text):
        l = len(text.split())
        return 5 <= l <= 120

    # ===============================
    # 🔥 NOISE FILTER
    # ===============================
    def is_noise(self, text):
        bad = [
            "equation", "formula", "figure", "table",
            "annexure", "model", "graph",
            "rainfall recharge", "methodology"
        ]
        return any(b in text for b in bad)

    # ===============================
    # 🔥 PROCESS RAW PDF (NO STRUCTURE HERE)
    # ===============================
    def process_raw_pdf(self, query, response):

        samples = []
        sentences = self.split_sentences(response)

        for i in range(len(sentences)):

            chunk = sentences[i]

            if i + 1 < len(sentences) and random.random() > 0.5:
                chunk += ". " + sentences[i+1]

            if not self.valid_length(chunk):
                continue

            if self.is_noise(chunk):
                continue

            samples.append({
                "query": self.rewrite_query(query, chunk),
                "response": chunk,
                "expert_tag": "raw_pdf"
            })

        return samples

    # ===============================
    # 🔹 BALANCE
    # ===============================
    def balance(self, domain, raw, dolly):

        structured = [d for d in raw if d["expert_tag"] == "structured"]
        normal_raw = [d for d in raw if d["expert_tag"] != "structured"]

        structured = structured[:1000]
        normal_raw = normal_raw[:500]

        raw = structured + normal_raw

        domain = domain[:1500]
        dolly = dolly[:200]

        final = domain + raw + dolly
        random.shuffle(final)

        print("\n📊 BALANCED DATASET")
        print(f"Domain       : {len(domain)}")
        print(f"Structured   : {len(structured)}")
        print(f"Raw Normal   : {len(normal_raw)}")
        print(f"Dolly        : {len(dolly)}")
        print(f"Total        : {len(final)}")

        return final

    # ===============================
    # 🚀 BUILD (UPDATED)
    # ===============================
    def build(self):

        print("\n🔱 STAGE 2: CLEAN + STRUCTURE + BALANCE\n")

        domain_data, raw_data, dolly_data = [], [], []

        for item in self.dataset:

            try:
                q = self.clean_text(item["query"])
                r = self.clean_text(item["response"])
                source = item["source"]
            except:
                continue

            if not q or not r:
                continue

            if source == "raw_pdf":
                raw_data.extend(self.process_raw_pdf(q, r))

            elif source == "domain":

                # 🔥 NORMAL DOMAIN SAMPLE
                if self.valid_length(r):
                    domain_data.append({
                        "query": self.rewrite_query(q, r),
                        "response": r,
                        "expert_tag": "domain"
                    })

                # 🔥 STRUCTURED EXTRACTION (KEY PART)
                structured_samples = self.extract_structured_from_domain(r)

                for s in structured_samples:
                    raw_data.extend([s] * 3)   # boost

            else:
                if self.valid_length(r):
                    dolly_data.append({
                        "query": self.rewrite_query(q, r),
                        "response": r,
                        "expert_tag": "dolly"
                    })

        final_dataset = self.balance(domain_data, raw_data, dolly_data)

        return final_dataset


