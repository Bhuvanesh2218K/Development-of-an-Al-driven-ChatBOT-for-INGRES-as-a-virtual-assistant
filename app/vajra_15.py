import numpy as np

class VajraBridgeEngine:
    def __init__(self, input_dim=384, topology_config=[256, 128, 64], num_classes=4, vocab_size=15000, gold_mask=None):
        self.dim = input_dim
        self.config = topology_config
        self.vocab_size = vocab_size
        self.num_classes = num_classes
        self.layers = []

        # --- DYNAMIC DIMENSION MAPPING ---
        self.dims = [input_dim]
        decay = (input_dim - 64) // len(topology_config)
        for i in range(len(topology_config)):
            if i == len(topology_config) - 1:
                self.dims.append(64)
            else:
                self.dims.append(self.dims[-1] - decay)

        # 🛰️ DYNAMIC GOLD MASK
        self.gold_mask = gold_mask if gold_mask is not None else np.full(vocab_size, 0.1, dtype=np.float32)

        # Frequency and Sequence Tracking
        self.freq_map = np.zeros(vocab_size, dtype=np.float32)
        self.pos_map = np.zeros(vocab_size, dtype=np.float32)

        # Initialize MoE Graph Layers
        for i in range(len(topology_config)):
            d_in = self.dims[i]
            d_out = self.dims[i+1]
            n_experts = topology_config[i]
            self.layers.append(VajraGraphLayer(n_in_experts=n_experts, dim_in=d_in, dim_out=d_out))

        final_dim = self.dims[-1]

        # --- ADAM OPTIMIZED HEADS ---
        self.head = (np.random.randn(final_dim, num_classes) * np.sqrt(2/final_dim)).astype(np.float32)
        self.m_head, self.v_head = np.zeros_like(self.head), np.zeros_like(self.head)

        self.speech_head = (np.random.randn(final_dim, vocab_size) * np.sqrt(1/final_dim)).astype(np.float32)

        self.m_speech, self.v_speech = np.zeros_like(self.speech_head), np.zeros_like(self.speech_head)

        self.history_vault = None
        self.ltm_buffer = None

    def snapshot_history(self):
        """🔱 SECTION V-C: THE REGULARIZATION ANCHOR"""
        self.history_vault = {
            "layers": [np.copy(l.W_experts) for l in self.layers],
            "head": np.copy(self.head),
            "speech_head": np.copy(self.speech_head)
        }
        self.ltm_buffer = np.zeros((1, self.dims[-1]), dtype=np.float32)

    def update_ltm(self, current_signal):
        if self.ltm_buffer is None:
            self.ltm_buffer = np.copy(current_signal)
        else:
            self.ltm_buffer = (0.85 * self.ltm_buffer) + (0.15 * current_signal)

    def calculate_hash_influence(self, token_id, pos):
        last_was_gold = self.gold_mask[token_id] >= 0.8
        if pos < 2: return 1.4
        if last_was_gold:
            influence = 0.65
        else:
            influence = 1.0 + (1.0 / (1.0 + np.exp(-(pos - 5)))) * 0.4

        freq = self.freq_map[token_id]
        if freq > 0:
            influence *= (1.0 / (1.0 + np.log1p(freq * 0.5)))
        return np.clip(influence, 0.1, 1.8)

    def forward(self, x, query_anchor=None, current_pos=0, last_token_id=None, is_training=True, target_expert=None):
        if x.ndim == 1: x = x.reshape(1, -1)
        current_signal = x

        # 1. Query Injection
        if query_anchor is not None:
            if query_anchor.ndim == 1: query_anchor = query_anchor.reshape(1, -1)
            anchor_weight = 0.3 if current_pos < 5 else 0.15
            current_signal = ((1 - anchor_weight) * current_signal) + (anchor_weight * query_anchor)

        all_caches = []
        # 2. Sequential MoE Processing
        for i, layer in enumerate(self.layers):
            current_signal, cache = layer.forward(current_signal, is_training=is_training)
            all_caches.append(cache)

            if i == len(self.layers) - 1 and self.ltm_buffer is not None:
                current_signal = (0.92 * current_signal) + (0.08 * self.ltm_buffer)

        # 3. GDI Attention
        current_signal = self.apply_gdi_attention(current_signal, is_training=is_training)

        # 4. Temporal Influence
        if last_token_id is not None:
            influence = self.calculate_hash_influence(last_token_id, current_pos)
            current_signal *= influence

        if not is_training:
            self.update_ltm(current_signal)

        # 5. Projections
        expert_logits = np.dot(current_signal, self.head)
        if target_expert is not None:
            expert_logits.fill(-1e4)
            expert_logits[0, target_expert] = 1e4

        word_logits = np.dot(current_signal, self.speech_head)
        word_logits = (word_logits - np.mean(word_logits)) / (np.std(word_logits) + 1e-8)

        # 6. Inference Warping
        if not is_training and self.gold_mask is not None:
            if current_pos % 2 == 0:
                word_logits += (self.gold_mask * 2.5)
            else:
                word_logits += ((1.0 - self.gold_mask) * 3.5)
                word_logits -= (self.gold_mask * 2.0)

        return expert_logits, word_logits, (all_caches, current_signal)

    def apply_gdi_attention(self, x, is_training=True):
         final_dim = self.dims[-1]
         gdi_scale = np.sqrt(final_dim)
         global_avg = np.mean(np.abs(x), axis=1, keepdims=True)
         importance_scores = global_avg * gdi_scale
         mask = np.where(importance_scores >= 0.5, 1.05, 0.95) if is_training else 1.0
         return x * mask

    def train_step_raw(self, x_word, q_anchor, target_expert_oh, target_word_indices, epoch=0, lambda_val=0.7, smoothing=0.1, pos=0):
        """🔱 VAJRA 11.2 UPGRADED TRAINING STEP"""
        batch_size = x_word.shape[0]

        for idx in target_word_indices:
            self.freq_map[idx] += 1
            self.pos_map[idx] = pos

        last_tok = target_word_indices[0] if pos > 0 else 1

        exp_logits, word_logits, (caches, final_out) = self.forward(
            x_word, query_anchor=q_anchor, current_pos=pos, last_token_id=last_tok, is_training=True
        )

        word_logits[:, [0, 1, 3]] = -1e9
        word_pred = self.softmax(word_logits)
        exp_pred = self.softmax(exp_logits)

        # --- LOSS A: ROUTING DOMAINS (Primary Priority) ---
        exp_loss = -np.mean(np.sum(target_expert_oh * np.log(exp_pred + 1e-10), axis=-1))

        # --- LOSS B: LINGUISTIC WORD PREDICTION ---
        mask_vals = self.gold_mask[target_word_indices]
        dynamic_smoothing = np.where(mask_vals >= 0.8, smoothing * 0.5, smoothing)
        smoothed_target = np.full_like(word_pred, (dynamic_smoothing / (self.vocab_size - 3))[:, np.newaxis])
        for b in range(batch_size):
            smoothed_target[b, target_word_indices[b]] = 1.0 - dynamic_smoothing[b]

        #loss_weights = np.where(mask_vals >= 0.8, 2.0, 4.0)
        loss_weights = mask_vals
        word_loss = np.mean(-np.sum(smoothed_target * np.log(word_pred + 1e-10), axis=-1) * loss_weights)

        # --- LOSS C: DIVERSITY PENALTY (Prevents Expert Collapse) ---
        router_probs = np.mean(exp_pred, axis=0)
        cv_squared = np.var(router_probs) / (np.mean(router_probs)**2 + 1e-10)
        load_balance_loss = 0.2 * cv_squared

        # --- LOSS D: CHECKPOINT REGULARIZATION (Historical Tethering) ---
        reg_loss = 0
        if self.history_vault is not None:
            for i, layer in enumerate(self.layers):
                # Penalize drift from the 'Solid' configuration
                reg_loss += 0.002 * np.mean((layer.W_experts - self.history_vault['layers'][i])**2)

        # TOTAL CONVERGENCE LOSS
        total_loss = (lambda_val * exp_loss) + ((1 - lambda_val) * word_loss) + load_balance_loss + reg_loss

        # Backward Flow
        d_exp_logits = (lambda_val * (exp_pred - target_expert_oh)) / batch_size
        d_word_logits = ((1 - lambda_val) * (word_pred - smoothed_target) * loss_weights[:, np.newaxis]) / batch_size
        d_word_logits = np.clip(d_word_logits, -0.1, 0.1)

        d_head = np.dot(final_out.T, d_exp_logits)
        d_speech_head = np.dot(final_out.T, d_word_logits)
        d_current_signal = np.dot(d_exp_logits, self.head.T) + np.dot(d_word_logits, self.speech_head.T)

        layer_grads = []
        for i in reversed(range(len(self.layers))):
            d_current_signal, grads = self.layers[i].backward(d_current_signal, caches[i])
            layer_grads.append(grads)

        return total_loss, d_head, d_speech_head, layer_grads[::-1], exp_pred, word_pred

    def apply_batch_update(self, d_head, d_speech_head, layer_grads, lr):
        self.apply_adam(self.head, d_head, self.m_head, self.v_head, lr, weight_decay=1e-4)
        self.apply_adam(self.speech_head, d_speech_head, self.m_speech, self.v_speech, lr, weight_decay=1e-5)
        for i, layer in enumerate(self.layers):
            dW, dR, dB, dAlpha = layer_grads[i]
            self.apply_adam(layer.W_experts, dW, layer.m_W, layer.v_W, lr)
            self.apply_adam(layer.W_router, dR, layer.m_R, layer.v_R, lr)
            self.apply_adam(layer.W_bridge, dB, layer.m_B, layer.v_B, lr)
            self.apply_adam(layer.bridge_alpha, dAlpha, layer.m_alpha, layer.v_alpha, lr,weight_decay=1e-2)

    def apply_adam(self, W, grad, m, v, lr, b1=0.9, b2=0.999, eps=1e-8, weight_decay=0.0):
        grad = np.clip(grad, -1.0, 1.0)
        if weight_decay > 0: grad += weight_decay * W
        m *= b1
        m += (1 - b1) * grad
        v *= b2
        v += (1 - b2) * (grad**2)
        m_hat = m / (1 - b1 + 1e-10)
        v_hat = v / (1 - b2 + 1e-10)
        W -= lr * m_hat / (np.sqrt(v_hat) + eps)

    def softmax(self, x):
        z = x - np.max(x, axis=-1, keepdims=True)
        e_x = np.exp(np.clip(z, -20, 20))
        return e_x / (np.sum(e_x, axis=-1, keepdims=True) + 1e-11)

import numpy as np

class VajraGraphLayer:
    def __init__(self, n_in_experts, dim_in, dim_out, confidence_threshold=0.7):
        self.dim_in = dim_in
        self.dim_out = dim_out
        self.n_experts = n_in_experts
        self.eps = 1e-9

        # 1. HIERARCHICAL WEIGHTS (Xavier/Kaiming Hybrid)
        std = np.sqrt(2.0 / (dim_in + dim_out))
        self.W_experts = (np.random.randn(n_in_experts, dim_in, dim_out) * std).astype(np.float32)

        # Router: symmetry breaking
        self.W_router = (np.random.randn(dim_in, n_in_experts) * 0.01).astype(np.float32)

        # 2. GATED BRIDGE (The Linguistic Highway)
        self.W_bridge = (np.random.randn(dim_in, dim_out) * std).astype(np.float32)

        # 🔱 PAPER SPEC FIX: Lowered from 0.5 to 0.4 to force MoE Expert engagement
        self.bridge_alpha = np.array([0.6], dtype=np.float32)

        # 3. RMSNorm parameters
        self.gamma = np.ones(dim_out, dtype=np.float32)
        self.beta = np.zeros(dim_out, dtype=np.float32)

        # 4. ADAM OPTIMIZER STATES
        self.m_W, self.v_W = np.zeros_like(self.W_experts), np.zeros_like(self.W_experts)
        self.m_R, self.v_R = np.zeros_like(self.W_router), np.zeros_like(self.W_router)
        self.m_B, self.v_B = np.zeros_like(self.W_bridge), np.zeros_like(self.W_bridge)
        self.m_alpha, self.v_alpha = np.zeros_like(self.bridge_alpha), np.zeros_like(self.bridge_alpha)

    def silu(self, x):
        return x * (1.0 / (1.0 + np.exp(-np.clip(x, -20, 20))))

    def rms_norm(self, x):
        rms = np.sqrt(np.mean(x**2, axis=-1, keepdims=True) + self.eps)
        return self.gamma * (x / rms) + self.beta

    def _softmax(self, x):
        z = x - np.max(x, axis=-1, keepdims=True)
        e_x = np.exp(np.clip(z, -20, 20))
        return e_x / (np.sum(e_x, axis=-1, keepdims=True) + 1e-11)

    def forward(self, x, is_training=True):
        if x.ndim == 1:
            x = x.reshape(1, -1)
        batch_size = x.shape[0]

        # --- PHASE 1: ROUTING WITH NOISE JITTER ---
        # 🔱 THE LOAD BALANCER: Adding noise during training to break path collision [21, 7, 4]
        clean_gates = np.dot(x, self.W_router)
        r_noise = np.random.normal(0, 0.1, clean_gates.shape) # 🔱 INCREASED NOISE
        gate_logits = clean_gates + r_noise
        #router_logits = np.dot(x, self.W_router) + r_noise
        gate_probs = self._softmax(gate_logits)

        # --- PHASE 2: EXPERT CONTRACTION ---
        all_expert_outputs = np.einsum('bi,eio->beo', x, self.W_experts)
        activated_experts = self.silu(all_expert_outputs)
        combined_expert_signal = np.einsum('beo,be->bo', activated_experts, gate_probs)

        # --- PHASE 3: GATED BRIDGE ---
        bridge_signal = np.dot(x, self.W_bridge)
        pre_norm = combined_expert_signal + (bridge_signal * self.bridge_alpha)

        output_signal = self.rms_norm(pre_norm)

        cache = {
            "input": x,
            "gate_probs": gate_probs,
            "expert_outputs": all_expert_outputs,
            "activated_experts": activated_experts,
            "bridge_signal": bridge_signal,
            "pre_norm": pre_norm
        }
        return output_signal, cache

    def backward(self, d_out, cache):
        x = cache["input"]
        gate_probs = cache["gate_probs"]
        expert_outputs = cache["expert_outputs"]
        activated_experts = cache["activated_experts"]
        bridge_signal = cache["bridge_signal"]
        batch_size = x.shape[0]

        # 1. RMSNorm Gradient
        pre_norm = cache["pre_norm"].reshape(batch_size, self.dim_out)
        rms = np.sqrt(np.mean(pre_norm**2, axis=-1, keepdims=True) + self.eps)
        d_pre_norm = (d_out * self.gamma / rms).reshape(batch_size, self.dim_out)

        # 2. Expert Path
        d_activated_experts = d_pre_norm[:, np.newaxis, :] * gate_probs[:, :, np.newaxis]
        sig = 1.0 / (1.0 + np.exp(-np.clip(expert_outputs, -20, 20)))
        d_expert_outputs = d_activated_experts * (sig * (1.0 + expert_outputs * (1.0 - sig)))

        d_W_experts = np.zeros_like(self.W_experts)
        for e in range(self.n_experts):
            d_W_experts[e] = np.dot(x.T, d_expert_outputs[:, e, :])

        W_exp_T = self.W_experts.transpose(0, 2, 1)
        d_x_experts = np.sum(np.matmul(d_expert_outputs[:, :, np.newaxis, :], W_exp_T), axis=1).reshape(batch_size, self.dim_in)

        # 3. Router Path
        d_gate_probs = np.sum(activated_experts * d_pre_norm[:, np.newaxis, :], axis=-1)
        d_router_logits = gate_probs * (d_gate_probs - np.sum(gate_probs * d_gate_probs, axis=-1, keepdims=True))
        d_W_router = np.dot(x.T, d_router_logits)
        d_x_router = np.dot(d_router_logits, self.W_router.T)

        # 4. Bridge & Alpha Path
        d_bridge_path = d_pre_norm * self.bridge_alpha
        d_W_bridge = np.dot(x.T, d_bridge_path)
        d_x_bridge = np.dot(d_bridge_path, self.W_bridge.T)

        d_alpha = np.array([np.sum(d_pre_norm * bridge_signal)], dtype=np.float32)

        d_x_total = (d_x_experts + d_x_router + d_x_bridge).reshape(batch_size, self.dim_in)

        return d_x_total, (d_W_experts, d_W_router, d_W_bridge, d_alpha)

