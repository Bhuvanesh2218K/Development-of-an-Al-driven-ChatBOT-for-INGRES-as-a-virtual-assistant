    # ===============================
    def silu(self, x):
        return x * (1.0 / (1.0 + np.exp(-np.clip(x, -20, 20))))

    # ===============================
    # 🔹 RMS NORM
    # ===============================
    def rms_norm(self, x):
        rms = np.sqrt(np.mean(x**2, axis=-1, keepdims=True) + self.eps)
        return self.gamma * (x / rms) + self.beta

    # ===============================
    # 🚀 FORWARD
    # ===============================
    def forward(self, x, is_training=True):

        if x.ndim == 1:
            x = x.reshape(1, -1)

        # 🔥 EXPERT COMPUTE
        expert_outputs = np.einsum('bi,eio->beo', x, self.W_experts)

        # activation
        activated = self.silu(expert_outputs)

        # average experts
        combined_expert_signal = np.mean(activated, axis=1)

        # 🔥 BRIDGE
        bridge_signal = np.dot(x, self.W_bridge)

        pre_norm = combined_expert_signal + self.bridge_alpha * bridge_signal

        output = self.rms_norm(pre_norm)

        cache = {
            "input": x,
            "expert_outputs": expert_outputs,
            "activated": activated,
            "bridge_signal": bridge_signal,
            "pre_norm": pre_norm
        }

        return output, cache

    # ===============================
    # 🔙 BACKWARD (FULLY FIXED)
    # ===============================
    def backward(self, d_out, cache):

        x = cache["input"]                        # (B, dim_in)
        expert_outputs = cache["expert_outputs"]  # (B, E, dim_out)
        bridge_signal = cache["bridge_signal"]

        # ===============================
        # 🔥 RMS NORM BACKWARD
        # ===============================
        pre_norm = cache["pre_norm"]
        rms = np.sqrt(np.mean(pre_norm**2, axis=-1, keepdims=True) + self.eps)
        d_pre = d_out * self.gamma / rms          # (B, dim_out)
        d_pre -= (pre_norm * np.mean(d_out * pre_norm, axis=-1, keepdims=True)) / (rms**3 + self.eps)
        # ===============================
        # 🔥 EXPERT PATH
        # ===============================
        d_W_experts = np.zeros_like(self.W_experts)

        sig = 1.0 / (1.0 + np.exp(-np.clip(expert_outputs, -20, 20)))

        d_x = np.zeros_like(x)

        for e in range(self.n_experts):

            exp_out = expert_outputs[:, e, :]     # (B, dim_out)

            d_act = d_pre / self.n_experts        # distribute

            # SiLU derivative
            d_exp = d_act * (sig[:, e, :] * (1 + exp_out * (1 - sig[:, e, :])))

            # weight gradient
            d_W_experts[e] = np.dot(x.T, d_exp) / (x.shape[0] + 1e-9)

            # input gradient (CORRECT)
            d_x += np.dot(d_exp, self.W_experts[e].T)

        # ===============================
        # 🔥 BRIDGE PATH
        # ===============================
        d_bridge = d_pre * self.bridge_alpha
        d_W_bridge = np.dot(x.T, d_bridge) / (x.shape[0] + 1e-9)

        d_alpha = np.array([np.sum(d_pre * bridge_signal)], dtype=np.float32)

        # add bridge input grad
        d_x += np.dot(d_bridge, self.W_bridge.T)

        return d_x, (d_W_experts, d_W_bridge, d_alpha)

