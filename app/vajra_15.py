def quick_generate(sample):

    query_vec = sample['query_vector']
    query_vec = query_vec / (np.linalg.norm(query_vec) + 1e-9)

    current_token = 1
    generated = []

    context = np.zeros(EMB_DIM, dtype=np.float32)

    for pos in range(15):

        curr_emb = embedding_matrix[current_token]

        # 🔥 CONTEXT UPDATE WITH QUERY
        context = 0.4 * context + 0.5 * curr_emb + 0.1 * query_vec

        # 🔥 POSITIONAL ADD
        context += POS_SCALE * positional_encoding(pos, EMB_DIM)

        # 🔥 NORMALIZE
        context = context / (np.linalg.norm(context) + 1e-9)

        x_word = np.concatenate([
            context,
            QUERY_SCALE * query_vec
        ]).reshape(1, -1)

        logits, _ = engine.forward(
            x_word,
            query_anchor=query_vec,
            current_pos=pos,
            is_training=False
        )

        probs = engine.softmax(logits)

        # 🚫 block special tokens
        probs[0][0] = probs[0][1] = probs[0][3] = 0

        # 🔥 SAFE NORMALIZATION
        probs = np.clip(probs, 0, None)
        s = np.sum(probs)

        if s <= 1e-12 or np.isnan(s):
            probs = np.ones_like(probs)
            probs /= np.sum(probs)
        else:
            probs /= s

        probs = probs / np.sum(probs)

        next_token = np.random.choice(len(probs[0]), p=probs[0])

        if next_token == 2:
            break

        generated.append(next_token)
        current_token = next_token

    return generated

