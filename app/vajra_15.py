class VajraEmbedder:

    def __init__(self, vocab):
        print("🚀 Loading embedding model...")

        self.model = SentenceTransformer('all-MiniLM-L6-v2')
        self.dim = 384
        self.vocab = vocab

        self.embedding_matrix = None
        self.build_embedding_matrix()

    # ===============================
    # 🔹 BUILD VOCAB EMBEDDINGS
    # ===============================
    def build_embedding_matrix(self):

        print("🛰️ Building embedding matrix...")

        words = [self.vocab.idx2word[i] for i in range(self.vocab.n_words)]

        vectors = self.model.encode(
            words,
            batch_size=64,
            show_progress_bar=True,
            convert_to_numpy=True
        )

        # normalize (IMPORTANT)
        norms = np.linalg.norm(vectors, axis=1, keepdims=True) + 1e-9
        vectors = vectors / norms

        self.embedding_matrix = vectors.astype(np.float32)

        print(f"✅ Embedding matrix ready: {self.embedding_matrix.shape}")

    # ===============================
    # 🔹 ENCODE QUERY
    # ===============================
    def encode(self, text):

        if not text or not text.strip():
            return np.zeros(self.dim, dtype=np.float32)

        vec = self.model.encode(text, convert_to_numpy=True)

        norm = np.linalg.norm(vec) + 1e-9
        vec = vec / norm

        return vec.astype(np.float32)

