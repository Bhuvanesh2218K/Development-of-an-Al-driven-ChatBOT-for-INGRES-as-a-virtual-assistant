def positional_encoding(pos, dim):
    pe = np.zeros(dim, dtype=np.float32)
    for i in range(0, dim, 2):
        pe[i] = np.sin(pos / (10000 ** (i / dim)))
        if i + 1 < dim:
            pe[i + 1] = np.cos(pos / (10000 ** (i / dim)))
    return pe

